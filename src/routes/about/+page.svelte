	About the Saudi Cyberfeminist Visual Index (SCFVI)
</h2>
<p>
	The Saudi Cyberfeminist Visual Index (SCFVI) is an open-source, web-based platform developed as
	part of an academic study. Rooted in feminist digital indexing practices and Saudi feminist
	ethics, the SCFVI serves as an interactive and searchable digital index documenting the Saudi
	Cyberfeminist Movement (SCM) from 2011 to 2021 and its continuing impact.
</p>
<p>
	Focusing on two pivotal campaigns—#EndMaleGuardianship and #Women2Drive—the SCFVI captures the
	narratives, strategies, and achievements of these landmark movements. By indexing social media
	posts, blog entries, images, and videos, the SCFVI safeguards these ephemeral materials from
	digital erasure while preserving the legacy of Saudi cyberfeminism.
</p>
<p>
	The SCFVI is more than an archive; it is a living, dynamic resource designed to function as a
	historical record, a pedagogical tool, and a reference for scholars, artists, and activists. By
	preserving the contributions of Saudi cyberfeminists, it enhances global understanding of digital
	feminist activism while emphasizing the intersection of gender, technology, and culture in Saudi
	Arabia.
</p>
